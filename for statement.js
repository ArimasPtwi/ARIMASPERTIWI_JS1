// Looping dengan penghitung
for (let i = 0; i < 10; i++) {
    console.log("Iterasi ke-" + (i + 1));
  }

  //Looping dengan elemen-elemen dalam sebuah array
  const fruits = ["Apel", "Pisang", "Jeruk", "Nanas"];
  for (let i = 0; i < fruits.length; i++) {
    console.log("Buah ke-" + (i + 1) + ": " + fruits[i]);
  }